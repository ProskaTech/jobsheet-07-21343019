public class Tugas2While {
    public static void main(String[] args) {
        int angka = 20;
        
        System.out.print("TAMPILAN URUTAN PENURUNAN ANGKA 20 - ANGKA 1 \n");
        while (angka >= 1)
        {
            System.out.println(angka);
            angka--;
        }
    }
}
